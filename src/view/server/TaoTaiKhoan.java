    private void readPrimaryUser() {
        listUsers.clear();
        String[] primaryUser = new String[4];
        if (readUser(primaryUser)) {
            for( String element : primaryUser){
                listUsers.add(element);
            }
        }
    }

    private void readOtherUsers(){
        String text = tp.getText().trim();
        if(text.isEmpty())
            return;
        String[] users = text.split("\n\n");
        for(String user:users){
            String[] items = user.split("\n");
            for(String item : items){
                listUsers.add(item.substring(item.indexOf(':') + 2));
            }
        }
    }
    
    private boolean readUser(String infor[]) {
        String soCMTND = tf_socmtnd_user.getText().trim();
        String hoTen = tf_hoten_user.getText().trim();
        Calendar ngaySinh = jDateChooser1.getCalendar();
        String diaChi = tf_diachi_user.getText().trim();
        if (soCMTND.isEmpty() || hoTen.isEmpty() || ngaySinh == null
                || diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập đủ thông tin");
            return false;
        }
        else if (!phu_tro.Verify.isNumeric(soCMTND)){
            JOptionPane.showMessageDialog(rootPane, "Số CMTND không hợp lệ");
            return false;
        }
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String ngaySinhString = dateFormat.format(ngaySinh.getTime());
        infor[0] = soCMTND;
        infor[1] = hoTen;
        infor[2] = ngaySinhString;
        infor[3] = diaChi;
        return true;
    }

