    private void btTaoTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTaoTKActionPerformed
        // TODO add your handling code here:
        String soTienKhoiTao = tf_sotienkhoitao.getText().trim();
        if(soTienKhoiTao.isEmpty() || !phu_tro.Verify.isNumeric(soTienKhoiTao)){
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập vào số tiền khởi tạo");
        }
        readPrimaryUser();
        if(listUsers.isEmpty()){
            return;
        }
        readOtherUsers();
        output.println("create account");
        output.println(soTienKhoiTao);
        listUsers.stream().forEach((element) -> {
            output.println(element);
        });
        output.println("out of information");
        resetFields();
        try {
            String result = input.readLine();
            JOptionPane.showMessageDialog(rootPane, result);
        } catch (IOException ex) {
            Logger.getLogger(TaoTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btTaoTKActionPerformed

